package my.model;



public class Cart {
	
	private int clothesId;
	
	
	
	private int amount;
	
	private int price;
	


	







	public int getClothesId() {
		return clothesId;
	}




	public int getPrice() {
		return price;
	}




	public void setPrice(int price) {
		this.price = price;
	}




	public void setClothesId(int clothesId) {
		this.clothesId = clothesId;
	}






	public int getAmount() {
		return amount;
	}


	public void setAmount(int amount) {
		this.amount = amount;
	}




}
